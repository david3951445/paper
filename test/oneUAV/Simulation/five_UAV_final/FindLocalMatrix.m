                                                        (cos(pv1(i2)*pi/180)*sin(pv2(j2)*pi/180)*cos(pv3(k2)*pi/180)+sin(pv1(i2)*pi/180)*sin(pv3(k2)*pi/180))/m 0 0 0;...
